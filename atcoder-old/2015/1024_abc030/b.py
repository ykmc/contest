import sys
input = sys.stdin.readline

# -------------------------------------------------------------
# function
# -------------------------------------------------------------


# -------------------------------------------------------------
# main
# -------------------------------------------------------------
N,M = map(int,input().split())

# 12時間表記に変換
N %= 12

# 時針と分針のなす角度
deg = abs(N*30+M*0.5 - M*6)

# 180度未満に変換
print(min(deg,360-deg))
