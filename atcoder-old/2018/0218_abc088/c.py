import sys
input = sys.stdin.readline

# -------------------------------------------------------------
# function
# -------------------------------------------------------------


# -------------------------------------------------------------
# main
# -------------------------------------------------------------
C = [list(map(int,input().split())) for _ in range(3)]

dx1 = C[1][0] - C[0][0]
dx2 = C[2][0] - C[1][0]
dy1 = C[0][1] - C[0][0]
dy2 = C[0][2] - C[0][1]

ans = "Yes"
for i in range(3):
    if C[1][i]-C[0][i] != dx1 or C[2][i]-C[1][i] != dx2:
        ans = "No"
        break
    if C[i][1]-C[i][0] != dy1 or C[i][2]-C[i][1] != dy2:
        ans = "No"
        break

print(ans)