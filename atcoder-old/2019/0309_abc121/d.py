import sys
input = sys.stdin.readline

# -------------------------------------------------------------
# function
# -------------------------------------------------------------
def f(n):
    if n%2==0:
        x = n//2
        if x%2==0:
            res = n
        else:
            res = n^1
    else:
        res = f(n-1)^n
    return res


# -------------------------------------------------------------
# main
# -------------------------------------------------------------
A,B = map(int,input().split())

print(f(B)^f(A-1))