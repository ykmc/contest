import sys
input = sys.stdin.readline

# -------------------------------------------------------------
# function
# -------------------------------------------------------------


# -------------------------------------------------------------
# main
# -------------------------------------------------------------
N,M = map(int,input().split())

if M < 2*N or 4*N < M:
    print(-1, -1, -1)
elif 3*N <= M <= 4*N:
    print(0, 4*N-M, M-3*N)
elif 2*N <= M < 3*N:
    print(3*N-M, M-2*N, 0)